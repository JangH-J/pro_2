	private int gongji_id, gongji_readnum;
	private String gongji_name, gongji_title, gongji_writeday, gongji_content;
	
	public int getGongji_id() {
		return gongji_id;
	}
	public void setGongji_id(int gongji_id) {
		this.gongji_id = gongji_id;
	}
